# Example input list (number_list_A)
number_list_A = [1, 2, 3, 4, 5]

# Empty list for results (number_list_B)
number_list_B = []

# Loop through number_list_A
for num in number_list_A:
    number_list_B.append(num * 2)

# Output results
print("Number List A:", number_list_A)
print("Number List B (each *2):", number_list_B)
